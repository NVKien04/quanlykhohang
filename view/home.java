    private void tablephieunhapMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablephieunhapMouseClicked
        TableModel model = tablephieunhap.getModel();
        int index = tablephieunhap.getSelectedRow();
        String id = model.getValueAt( index,0).toString();
        String user = model.getValueAt( index,1).toString();     
        String quantity = model.getValueAt(index,2).toString();
        String khuvuc= model.getValueAt(index,3).toString();
        String date = model.getValueAt(index,4).toString();
        
        DateTimeFormatter inputFormatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        DateTimeFormatter outputFormatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        
        
        
        jTextField7.setText(id);
        jTextField8.setText(user);
        jTextField9.setText(quantity);
        jTextField10.setText(khuvuc);  
        try {
            LocalDate Date = LocalDate.parse(date, inputFormatter);

            // Định dạng lại LocalDate thành chuỗi với định dạng mong muốn
            String formattedDate = Date.format(outputFormatter);    
            jTextField18.setText(formattedDate);
        } catch (DateTimeParseException e) {
            JOptionPane.showMessageDialog(rootPane, "\"Chuỗi ngày không hợp lệ: \" + date");
        }
    }//GEN-LAST:event_tablephieunhapMouseClicked

