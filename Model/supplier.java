package Model;

/**
 *
 * @author Dell
 */
public class supplier {
    private String id, name, address,email,sdt;

    public supplier() {
    }

    public supplier(String id, String name, String address, String email, String sdt) {
        this.id = id;
        this.name = name;
        this.address = address;
        this.email = email;
        this.sdt = sdt;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSdt() {
        return sdt;
    }

    public void setSdt(String sdt) {
        this.sdt = sdt;
    }
    
}
