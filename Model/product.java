package Model;

/**
 *
 * @author Dell
 */
public class product {
    private String id, name, quantity, price, brand, os, supplier, wareHouse;

    public product() {
    }

    public product(String id, String name, String quantity, String price, String brand, String os, String supplier, String wareHouse) {
        this.id = id;
        this.name = name;
       
        this.price = price;
        this.brand = brand;
        this.os = os;
        this.supplier = supplier;
        this.wareHouse = wareHouse;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

   

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getOs() {
        return os;
    }

    public void setOs(String os) {
        this.os = os;
    }

    public String getSupplier() {
        return supplier;
    }

    public void setSupplier(String supplier) {
        this.supplier = supplier;
    }

    public String getWareHouse() {
        return wareHouse;
    }

    public void setWareHouse(String wareHouse) {
        this.wareHouse = wareHouse;
    }
    
    
    
}
