package Model;

/**
 *
 * @author Dell
 */
public class employee {
    private String code, name, gender, position,phone,email;

    public employee(String code, String name, String gender, String position, String phone, String email) {
        this.code = code;
        this.name = name;
        this.gender = gender;
        this.position = position;
        this.phone = phone;
        this.email = email;
    }

    public employee() {
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    
}
